function val=get(obj,name)
switch name
    case 'ang'
        val=obj.ang;
    case 'angv'
        val=obj.angv;
    case 'D'
        val=obj.D;
    case 'X'
        val=obj.X;
    case 'neigh'
        val=obj.neigh;
    case 'PB'
        val=obj.PB;
    case 'M'
        val=obj.M;
end